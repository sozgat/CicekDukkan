package com.model;

public class DistanceCompany {

	private double redDistance;
	private double greenDistance;
	private double blueDistance;
	
	public double getRedDistance() {
		return redDistance;
	}
	public void setRedDistance(double redDistance) {
		this.redDistance = redDistance;
	}
	public double getGreenDistance() {
		return greenDistance;
	}
	public void setGreenDistance(double greenDistance) {
		this.greenDistance = greenDistance;
	}
	public double getBlueDistance() {
		return blueDistance;
	}
	public void setBlueDistance(double blueDistance) {
		this.blueDistance = blueDistance;
	}
}
